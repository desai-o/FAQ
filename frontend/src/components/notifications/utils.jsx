function getNotificationId(notification) {
  return notification.id || notification._id;
}

function isNotificationRead(notification) {
  return Boolean(notification.isRead ?? notification.is_read);
}

function getNotificationCreatedAt(notification) {
  return notification.createdAt || notification.created_at;
}

function getNotificationType(notification) {
  if (notification.type) return notification.type;
  const msg = (notification.message || "").toLowerCase();
  if (msg.includes("answer") || msg.includes("replied")) return "answer";
  if (msg.includes("upvote") || msg.includes("up vote") || msg.includes("liked")) return "upvote";
  if (msg.includes("flag") || msg.includes("warning") || msg.includes("report")) return "warning";
  if (msg.includes("follow") || msg.includes("following")) return "follow";
  if (msg.includes("system") || msg.includes("announcement")) return "system";
  return "general";
}

function formatRelativeTime(dateString) {
  if (!dateString) return "";
  const now = new Date();
  const date = new Date(dateString);
  const diffMs = now - date;
  const diffMins = Math.floor(diffMs / 60000);
  const diffHours = Math.floor(diffMs / 3600000);
  const diffDays = Math.floor(diffMs / 86400000);

  if (diffMins < 1) return "Just now";
  if (diffMins < 60) return `${diffMins}m ago`;
  if (diffHours < 24) return `${diffHours}h ago`;
  if (diffDays < 7) return `${diffDays}d ago`;
  return date.toLocaleDateString();
}

